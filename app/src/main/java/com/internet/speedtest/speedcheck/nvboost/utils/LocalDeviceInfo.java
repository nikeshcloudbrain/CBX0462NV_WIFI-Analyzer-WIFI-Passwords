package com.internet.speedtest.speedcheck.nvboost.utils;

public class LocalDeviceInfo
{


      String ip, mac;

      public LocalDeviceInfo(String Ip, String Mac) {
         this.ip = Ip;
         this.mac = Mac;

      }

      public LocalDeviceInfo() {

      }

      public String getIp() {
         return ip;
      }

      public void setIp(String date) {
         this.ip = date;
      }

      public String getMac() {
         return mac;
      }

      public void setMac(String amount) {
         this.mac = amount;
      }


   }



